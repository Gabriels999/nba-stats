let LesteWinChart = []
for (i = 0; i < nbaLeague.conferencias.leste.length; i++) {
  LesteWinChart.push(nbaLeague.conferencias.leste[i].w)
}
LesteWinChart.sort((a, b) => b - a)

function LesteFirstPlace() {
  let winner = LesteWinChart[0]
  for (i = 0; i < nbaLeague.conferencias.leste.length; i++) {
    if (nbaLeague.conferencias.leste[i].w == winner) {
      console.log(
        `O melhor colocado na conferência Leste foi: ${nbaLeague.conferencias.leste[i].name}.`
      )
    }
  }
}

function LesteLastPlace() {
  LesteWinChart.reverse()
  let loser = LesteWinChart[0]
  for (i = 0; i < nbaLeague.conferencias.leste.length; i++) {
    if (nbaLeague.conferencias.leste[i].w == loser) {
      console.log(
        `O pior colocado na conferência Leste foi: ${nbaLeague.conferencias.leste[i].name}.`
      )
    }
  }
  LesteWinChart.reverse()
}

function LesteTopThreePlaces() {
  let bestThree = []
  let bestThreeNames = []

  for (i = 0; i <= 2; i++) {
    bestThree.push(LesteWinChart[i])
  }
  for (i = 0; i < bestThree.length; i++) {
    for (j = 0; j < nbaLeague.conferencias.leste.length; j++) {
      if (
        nbaLeague.conferencias.leste[j].w == bestThree[i] &&
        bestThreeNames.length < 3
      ) {
        bestThreeNames.push(nbaLeague.conferencias.leste[j].name)
      }
    }
  }
  console.log(`Os três melhores na conferência Leste foram:${bestThreeNames}.`)
}
function LesteTeams() {
  console.log('Os times da conferência Leste são:')
  for (i = 0; i < nbaLeague.conferencias.leste.length; i++) {
    console.log(nbaLeague.conferencias.leste[i].name)
  }
}

